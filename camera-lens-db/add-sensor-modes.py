def main():
    with open("camera-lens-db/camera-lens-database.json", "r") as f:
        db = json.load(f)

    matched = 0
    skipped_film = 0
    no_data = 0

    for camera in db["cameras"]:
        if camera.get("category") == "Film Camera":
            skipped_film += 1
            continue

        name = camera["name"]
        if name in SENSOR_MODES:
            camera["sensor_modes"] = SENSOR_MODES[name]
            matched += 1
        else:
            no_data += 1
            print(f"WARNING: No sensor mode data for: {camera['manufacturer']} {name}", file=sys.stderr)

    with open("camera-lens-db/camera-lens-database.json", "w") as f:
        json.dump(db, f, indent=2)

    print(f"Done: {matched} cameras updated, {skipped_film} film cameras skipped, {no_data} digital cameras without data")


if __name__ == "__main__":
    main()
